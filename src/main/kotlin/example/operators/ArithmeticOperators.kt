package example.operators

fun main () {
    val number1 = 12.5
    val number2 = 3.5

    val result: Double = number1 + number2
    println("number1 + number2 = $result")

    val result2 : Double = number1 - number2
    println("number1 - number2 = $result2")

    val result3 : Double = number1 * number2
    println("number1 * number2 = $result3")

    val result4 : Double = number1 / number2
    println("number1 / number2 = $result4")

    val result5 : Double = number1 % number2
    println("number1 % number2 = $result5")

}

